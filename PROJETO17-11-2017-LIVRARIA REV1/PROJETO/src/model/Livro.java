package model;

public class Livro {
	
	private String livronome;
	private String livroidioma;
	private int livroano;
	
	public String getLivronome() {
		return livronome;
	}
	public void setLivronome(String livronome) {
		this.livronome = livronome;
	}
	public String getLivroidioma() {
		return livroidioma;
	}
	public void setLivroidioma(String livroidioma) {
		this.livroidioma = livroidioma;
	}

	public int getLivroano() {
		return livroano;
	}
	public void setLivroano(int livroano) {
		this.livroano = livroano;
	}		
}
