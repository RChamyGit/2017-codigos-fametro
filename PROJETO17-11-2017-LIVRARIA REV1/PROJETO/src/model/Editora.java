package model;

public class Editora {
	
	private int editorafone;
	private String editoranome;
	private String editoraendereco;
	

	
	public int getEditorafone() {
		return editorafone;
	}
	public void setEditorafone(int editorafone) {
		this.editorafone = editorafone;
	}
	public String getEditoranome() {
		return editoranome;
	}
	public void setEditoranome(String editoranome) {
		this.editoranome = editoranome;
	}
	public String getEditoraendereco() {
		return editoraendereco;
	}
	public void setEditoraendereco(String editoraendereco) {
		this.editoraendereco = editoraendereco;
	}
	
}
